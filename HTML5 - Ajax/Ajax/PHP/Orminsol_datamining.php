<ul type="square"><span style="font-weight:bold;">Description de la mission :</span> 
<li>Réaliser un applicatif marketing sous Excel de datamining permettant de déterminer les meilleures règles d'association de produits au sein d'un catalogue de VPC. </li>
</ul>

<ul type="square"><span style="font-weight:bold;">Utilité de la mission :</span>
<li>Connaître les meilleures règles d'association de produits afin de pouvoir élaborer les offres marketing les plus pertinentes.</li>
<li>Optimiser la mise en page du catalogue de VPC en réunissant les produits dont l'association est la plus judicieuse. </li>
</ul>

<ul type="square"><span style="font-weight:bold;">Sous activités mobilisées :</span>
<li>Mettre en place un système d'import et de formatage des données brutes de vente.</li>
<li>Elaborer un outil sous Excel de scan des données de vente.</li>
<li>Transcrire l'algorithme de type A-priori sous VBA Excel.</li>
<li>Etablir une interface graphique de sortie simple à comprendre.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Qualités requises :</span>
<li>Créativité.</li>
<li>Logique et analyse.</li>
<li>Esprit de synthèse.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Compétences nécessaires :</span>
<li>Maîtrise du VBA Excel.</li>
<li>Notions de datamining.</li>
<li>Bon sens marketing.</li>
<li>Connaissances algorithmiques.</li>
</ul>

