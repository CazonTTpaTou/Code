<ul type="square"><span style="font-weight:bold;">Description de la mission :</span> 
<li>Programmation d'un applicatif de simulation des stocks de matière première en Java.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Utilité de la mission :</span>
<li>Eviter les éventuels ruptures de stock et les retards de commande.</li>
<li>Limiter au maximum les accumulations de stock afin de maîtriser les besoins en fond de roulement de l'entreprise.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Sous activités mobilisées :</span>
<li>Simuler la demande des clients de l'entreprise à l'aide de lois de probabilités (loi de Weibull, loi Gamma).</li>
<li>Modéliser les fluctuations de l'approvisionnement des matières premières.</li>
<li>Réaliser une interface graphique simple et fonctionnelle.</li>
<li>Programmer une application Java à l'aide de l'IDE Eclipse Helios.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Qualités requises :</span>
<li>Curiosité.</li>
<li>Rigueur.</li>
<li>Précision.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Compétences nécessaires :</span>
<li>Quelques notions de logistique et de comptabilité des stocks.</li>
<li>Maîtrise de la programmation Objet en java et de l'environnement Eclipse.</li>
<li>Des connaissances en probabilités et modélisation des phénomènes stochastiques.</li>
<li>Utilisation des différents diagrammes UML de spécifications.</li>
</ul>

