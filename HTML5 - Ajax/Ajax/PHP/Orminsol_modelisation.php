<ul type="square"><span style="font-weight:bold;">Description de la mission :</span> 
<li>Modéliser les prévisions de chiffres d'affaires des opérations de mailing en fonction de paramètres déterminés.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Utilité de la mission :</span>
<li>Fournir des données d'aide à la décision à la direction marketing de l'entreprise.</li>
<li>Effectuer des prévisions de rentabilité pour établir le budget d'une opération de mailing.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Sous activités mobilisées :</span>
<li>Déterminer les paramètres les plus pertinents pour expliquer la réalisation du chiffre d'affaires.</li>
<li>Modéliser les fluctuations de chiffre d'affaires d'une opération de mailing à l'autre.</li>
<li>Comparer résultats théoriques et résultats empiriques pour réduire les erreurs et affiner le modèle.</li>
<li>Estimer la pertinence des différents paramètres par le biais de tests statistiques d'hypothèses.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Qualités requises :</span>
<li>Réflexion.</li>
<li>Analyse.</li>
<li>Capacités d'abstraction.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Compétences nécessaires :</span>
<li>Maîtrise du logiciel R.</li>
<li>Connaissance des techniques statistiques de régression.</li>
<li>Utilisation des tests statistiques d'hypothèses.</li>
<li>Notions de gestion et de marketing.</li>
</ul>

