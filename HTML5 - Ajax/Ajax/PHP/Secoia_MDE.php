</ul>

<ul type="square"><span style="font-weight:bold;">Description de la mission :</span> 
<li>Rédiger des modes d'emploi utilisateur et des guides d'utilisation.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Utilité de la mission :</span>
<li>Guider l'utilisateur vers une bonne utilisation du logiciel livré.</li>
<li>Avertir l'utilisateur des erreurs à ne pas commettre et des scénarios à éviter. </li>
</ul>

<ul type="square"><span style="font-weight:bold;">Sous activités mobilisées :</span>
<li>Rédiger des notices claires, intelligibles et détaillées.</li>
<li>Penser et imaginer les différents cas d'erreurs dans lesquels l'utilisateur pourrait s'engluer.</li>
<li>Anticiper les éventuels blocages dont pourra être victime l'utilisateur.</li>
<li>Vulgariser le vocabulaire technique de l'environnement du logiciel livré.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Qualités requises :</span>
<li>Patience.</li>
<li>Humilité.</li>
<li>Etre à l'écoute.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Compétences nécessaires :</span>
<li>Sens de la pédagogie.</li>
<li>Aptitudes à rédiger de façon claire et intelligible.</li>
<li>Capacités à expliciter simplement des problèmes complexes.</li>
<li>Savoir être pragmatique et pouvoir aller à l'essentiel sans vouloir trop détailler.</li>
</ul>

