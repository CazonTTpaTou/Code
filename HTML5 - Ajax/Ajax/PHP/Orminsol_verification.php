<ul type="square"><span style="font-weight:bold;">Description de la mission :</span> 
<li>Réaliser des tests statistiques de différentes hypothèses marketing.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Utilité de la mission :</span>
<li>Faciliter les prises de décision concernant les décisions d'investissement en prospection commerciale.</li>
<li>Affiner les différentes connaissances marketing concernant la clientèle et les fichiers de prospection de l'entreprise.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Sous activités mobilisées :</span>
<li>Réaliser des échantillons de fichier de prospection.</li>
<li>Vérifier la conformité des échantillons par rapport à leur fichier source.</li>
<li>Comparer les résultats de différents échantillons.</li>
<li>Inférer les caractéristiques d'un fichier à partir des résultats de son échantillon.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Qualités requises :</span>
<li>Rigueur et méthode.</li>
<li>Capacités d'abstraction.</li>
<li>Analyse et réflexion.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Compétences nécessaires :</span>
<li>Maîtrise du logiciel R.</li>
<li>Connaissances de la statistique inférentielle.</li>
<li>Utilisation des méthodes aléatoires d'échantillonage.</li>
<li>Notions de marketing direct.</li>
</ul>

