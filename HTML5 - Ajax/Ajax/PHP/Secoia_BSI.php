<ul type="square"><span style="font-weight:bold;">Description de la mission :</span> 
<li>Reconstituer des données salariales pour effectuer des BSI (bilan social individuel).</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Utilité de la mission :</span>
<li>Travailler à partir de données propres, cohérentes et unifiées. </li>
<li>Détecter les éventuelles erreurs et affiner les données transmises.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Sous activités mobilisées :</span>
<li>Reconstituer des salaires incomplets sur des bases temps plein.</li>
<li>Effectuer des projections et des revalorisations salariales.</li>
<li>Calculer les points retraite pour les périodes de cotisation manquantes.</li>
<li>Déceler des données incohérentes et intercepter les anomalies.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Qualités requises :</span>
<li>Rigueur.</li>
<li>Vigilance.</li>
<li>Sens du détail.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Compétences nécessaires :</span>
<li>Maîtrise de Access.</li>
<li>Notions de calcul retraite.</li>
<li>Connaissance du droit social.</li>
<li>Manipulation de gros volume de données.</li>
</ul>

