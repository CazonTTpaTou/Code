<ul type="square"><span style="font-weight:bold;">Description de la mission :</span> 
<li><Création de modules Excel de simulation et de projection de prestations sociales./li>
</ul>

<ul type="square"><span style="font-weight:bold;">Utilité de la mission :</span>
<li>Estimer les coûts potentiels d'une prestation sociale.</li>
<li>Projeter les différents coûts d'une prestation sociale à verser dans le futur.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Sous activités mobilisées :</span>
<li>Modéliser la logique de calcul de la prestation.</li>
<li>Effectuer des vérifications juridiques sur les conditions d'application de la prestation sociale.</li>
<li>Programmer l'algorithme modélisant la prestation considérée.</li>
<li>Vérifier et tester la cohérence du module réalisé à partir de jeux de données.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Qualités requises :</span>
<li>Réflexion et capacités de synthèse.</li>
<li>Analyse et esprit critique.</li>
<li>Sens du détail et minutie.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Compétences nécessaires :</span>
<li>Maîtrise de Excel et des formules matricielles élaborées.</li>
<li>Notions de droit social.</li>
<li>Capacités à chercher de l'information juridique.</li>
<li>Aptitudes à formaliser un problème concret en algorithme et en solution logicielle.</li>
</ul>

