</ul>

<ul type="square"><span style="font-weight:bold;">Description de la mission :</span> 
<li>Réaliser des états comptables de passif social aux normes IFRS.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Utilité de la mission :</span>
<li>Déterminer le montant du passif social à porter au bilan.</li>
<li>Etablir le montant de la charge et de la dotation à porter au compte de résultat.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Sous activités mobilisées :</span>
<li>Etablir un état comptable de clôture.</li>
<li>Elaborer un état comptable prévisionnel.</li>
<li>Déterminer la charge financière (coût de l'actualisation).</li>
<li>Décider des éventuelles applications de la réglementation en vigueur (amortissement corridor).</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Qualités requises :</span>
<li>Rigueur.</li>
<li>Précision.</li>
<li>Pragmatisme.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Compétences nécessaires :</span>
<li>Connaissances comptables de base.</li>
<li>Notions des normes IFRS.</li>
<li>Utilisation des mathématiques financières (actualisation).</li>
<li>Maîtrise des tableaux Excel et des fonctions financières.</li>
</ul>

