<ul type="square"><span style="font-weight:bold;">Description de la mission :</span> 
<li>Mise en place d'un applicatif sous Excel de gestion des loyers.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Utilité de la mission :</span>
<li>Assurer l'édition des quittances de loyer et la gestion des relances.</li>
<li>Automatiser l'actualisation annuelle des loyers et la mise à jour des indices.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Sous activités mobilisées :</span>
<li>Automatiser le processus de gestion des loyers d'un parc de locaux commerciaux.</li>
<li>Editer des quittances de loyer selon les règles en vigeur.</li>
<li>Etablir des actualisations conformes aux baux commerciaux signés.</li>
<li>Assurer les relances automatiques passé le délai en vigueur.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Qualités requises :</span>
<li>Rigueur et précision.</li>
<li>Sens du détail.</li>
<li>Sens de l'interprétation juridique.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Compétences nécessaires :</span>
<li>Maîtriser le VBA Excel.</li>
<li>Savoir élaborer des interfaces graphiques sous Excel.</li>
<li>Connaître l'utilisation des indices statistiques.</li>
<li>Avoir des notions de droit locatif.</li>
</ul>

