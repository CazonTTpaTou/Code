<ul type="square"><span style="font-weight:bold;">Description de la mission :</span> 
<li>Rédaction de commentaires détaillés pour les rapports de calcul de provision.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Utilité de la mission :</span>
<li>Fournir au client tous les éléments pour comprendre les chiffres de la provision comptable qui lui sont transmis.</li>
<li>Anticiper toutes les éventuelles questions et interrogations du client à la lecture des résultats communiqués.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Sous activités mobilisées :</span>
<li>Faire l'inventaire des différents de choix de paramètres.</li>
<li>Expliquer le choix des paramètres retenus pour l'évaluation comptable.</li>
<li>Analyser et commenter les résultats obtenus.</li>
<li>Commenter les éventuels éléments exceptionnels qui nécessitent un approfondissement particulier.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Qualités requises :</span>
<li>Disponibilité.</li>
<li>Humilité.</li>
<li>Sens de l'écoute.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Compétences nécessaires :</span>
<li>Capacités rédactionnelles.</li>
<li>Savoir être pédagoge.</li>
<li>Être capable se mettre à la place du client.</li>
<li>Aptitudes à l'argumentation.</li>
</ul>

