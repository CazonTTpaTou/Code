<ul type="square"><span style="font-weight:bold;">Description de la mission :</span> 
<li>Réalisation de calcul d'engagement de passifs sociaux d'entreprise (indemnité fin de carrière, droits à la retraite, formation, mutuelle santé).</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Utilité de la mission :</span>
<li>Etablir le montant du passif social de l'entreprise qui représente la dette actuarielle des prestations que l'entreprise aura à verser à ses salariés dans le futur.</li>
<li>Fournir à l'entreprise cliente un rapport de calcul qui détaille et analyse la constitution du montant de son engagement retraite.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Sous activités mobilisées :</span>
<li>Vérifier le fichier salarial des données sources.</li>
<li>Etablir les paramètres et hypothèses de calcul appropriés.</li>
<li>Concevoir des états comptables aux normes IFRS.</li>
<li>Rédiger des rapports de calcul détaillés.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Qualités requises :</span>
<li>Précision.</li>
<li>Rigueur.</li>
<li>Vigilance.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Compétences nécessaires :</span>
<li>Maîtrise de Excel.</li>
<li>Utilisation du logiciel actuariel LEAIFC.</li>
<li>Notions des normes IFRS.</li>
<li>Connaissance de la législation des retraites.</li>
</ul>

