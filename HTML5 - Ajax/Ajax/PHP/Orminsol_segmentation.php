<ul type="square"><span style="font-weight:bold;">Description de la mission :</span> 
<li>Segmenter le fichier des clients de l'entreprise selon des critères socio-marketing.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Utilité de la mission :</span>
<li>Avoir une meilleure connaissance des différentes typologies de clientèle de l'entreprise.</li>
<li>Proposer des offres marketing sur mesure les plus adaptées possibles aux différents segments de clientèle.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Sous activités mobilisées :</span>
<li>Vérifier les critères de segmentation par ACP (analyse en composantes principales).</li>
<li>Etablir les modalités les plus représentatives de chaque fichier de prospection par le biais d'une AFC (analyse factorielle de correspondances).</li>
<li>Déterminer des segments de clientèle discriminants par le biais d'AFCM (analyse factorielle en composantes multiples).</li>
<li>Vérifier les classes de clientèle obtenues par la réalisation de classification hiérarchique ascendante (CAH).</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Qualités requises :</span>
<li>Esprit de synthèse.</li>
<li>Capacités d'abstraction.</li>
<li>Analyse et réflexion.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Compétences nécessaires :</span>
<li>Connaissance des techniques statistiques d'analyse des données.</li>
<li>Maîtrise du logiciel R.</li>
<li>Notions de datamining et des techniques de classification.</li>
<li>Sens de l'analyse marketing.</li>
</ul>

