<ul type="square"><span style="font-weight:bold;">Description de la mission :</span> 
<li>Mettre en place un système de gestion de bases de données relationnelles sous Access pour la gestion de la société.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Utilité de la mission :</span>
<li>Réunir en une seule application tous les outils de gestion de la société (gestion commerciale, comptabilité, marketing, logistique).</li>
<li>Apporter de la valeur ajoutée aux données contenues dans la base (meilleure connaissance de la clientèle, gestion marketing, pilotage de la logistique).</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Sous activités mobilisées :</span>
<li>Construction d'un SGBD sous Access.</li>
<li>Elaboration d'interfaces graphiques fonctionnelles et conviviales.</li>
<li>Mise en place d'un applicatif de gestion de la logistique.</li>
<li>Mise en place d'un outil de pilotage du marketing.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Qualités requises :</span>
<li>Créativité.</li>
<li>Organisation.</li>
<li>Méthode.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Compétences nécessaires :</span>
<li>Maîtrise de la base de données Access.</li>
<li>Connaissance de VBA Access.</li>
<li>Ecriture de requêtes SQL élaborées.</li>
<li>Notions de marketing et de processus logistiques.</li>
</ul>

