<ul type="square"><span style="font-weight:bold;">Description de la mission :</span> 
<li>Réaliser des rapports marketing synthétisant les différents indicateurs des opérations de mailing réalisées.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Utilité de la mission :</span>
<li>Fournir à la direction de l'entreprise des données d'aide à la décision.</li>
<li>Réaliser une synthèse des différents indicateurs marketing de la clientèle de l'entreprise.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Sous activités mobilisées :</span>
<li>Agréger et formater sous Excel les différentes données de vente des opérations de mailing.</li>
<li>Réaliser des indicateurs fournissant du sens et de la connaissance pertinente sur les derniers résultats de vente.</li>
<li>Construire des courbes et graphiques suffisamment explicites pour apporter de l'information visuelle utile.</li>
<li>Rédiger des rapports expliquant les principaux enseignements à retenir de l'opération de mailing effectuée.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Qualités requises :</span>
<li>Esprit de synthèse.</li>
<li>Capacités rédactionnelles.</li>
<li>Aptitudes pédagogiques.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Compétences nécessaires :</span>
<li>Maîtriser Excel et les différentes fonctions graphiques.</li>
<li>Connaissances du marketing direct et de la vente à distance.</li>
<li>Aptitudes à réaliser des indicateurs précis et synthétiques.</li>
<li>Utilisation des différentes techniques statistiques de segmentation.</li>
</ul>

