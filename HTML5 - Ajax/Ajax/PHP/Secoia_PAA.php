<ul type="square"><span style="font-weight:bold;">Description de la mission :</span> 
<li>Programmation en VBA d'une application actuarielle pour BP et Mobil.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Utilité de la mission :</span>
<li>Simuler le coût du départ de salariés dans le cadre d'un plan social.</li>
<li>Calculer la répartition de ces coûts entre les entreprises BP et Mobil.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Sous activités mobilisées :</span>
<li>Réaliser un module de simulation des coûts des prestations versées dans le cadre d'un licenciement en plan social.</li>
<li>Concevoir un module de répartition de ces coûts en fonction de la carrière de chaque salarié.</li>
<li>Etablir un module calculant la facturation totale de chacune des deux entreprises sur l'intégralité d'un semestre.</li>
<li>Elaborer un module éditant un rapport de facturation comportant toutes les statistiques relatives aux calculs générés.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Qualités requises :</span>
<li>Analyse et réflexion.</li>
<li>Volonté et ténacité.</li>
<li>Discipline et minutie.</li>
</ul>

<ul type="square"><span style="font-weight:bold;">Compétences nécessaires :</span>
<li>Maîtrise du VBA Excel.</li>
<li>Connaissance des spécifications UML.</li>
<li>Notions de droit social.</li>
<li>Aptitude à gérer des spécifications complexes..</li>
</ul>

